  // Code-focused roles get Codex (o4-mini), research/analysis roles get Claude
  const codeRoles = new Set(["builder", "sentinel"]);

  let currentPrompts = buildAgentPrompts(profile);

  const AGENT_PREAMBLE = currentPrompts.preamble;

