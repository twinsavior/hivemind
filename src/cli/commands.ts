// ── Prompt helper ─────────────────────────────────────────────────────────────

async function prompt(question: string, defaultValue?: string): Promise<string> {
  const rl = readline.createInterface({
    input: process.stdin,
    output: process.stdout,
  });

  const suffix = defaultValue ? ` (${defaultValue})` : "";

  return new Promise((resolve) => {
    rl.question(`  ${question}${suffix}: `, (answer) => {
      rl.close();
      resolve(answer.trim() || defaultValue || "");
    });
  });
}

// ── init ──────────────────────────────────────────────────────────────────────

interface InitOptions {
  yes?: boolean;
  template?: string;
}

export async function initCommand(options: InitOptions): Promise<void> {
  const cwd = process.cwd();

  if (fs.existsSync(path.join(cwd, "hivemind.yaml"))) {
    warn("A hivemind.yaml already exists in this directory.");
    const overwrite = await prompt("Overwrite? (y/N)", "N");
    if (overwrite.toLowerCase() !== "y") {
      info("Init cancelled.");
      return;
    }
  }

  let projectName = path.basename(cwd);
  let primaryModel = "anthropic/claude-sonnet-4-20250514";
  let enableDashboard = true;
  let dashboardPort = 3000;

  if (!options.yes) {
    projectName = await prompt("Project name", projectName);
    primaryModel = await prompt("Primary LLM model", primaryModel);
    const dashAnswer = await prompt("Enable web dashboard?", "yes");
    enableDashboard = ["yes", "y", "true"].includes(dashAnswer.toLowerCase());
    if (enableDashboard) {
      const portAnswer = await prompt("Dashboard port", "3000");
      dashboardPort = parseInt(portAnswer, 10) || 3000;
    }
  }

  const s = spinner("Scaffolding project...");

  const configContent = `# HIVEMIND Configuration
# Generated by \`hivemind init\`

name: "${projectName}"
version: "1.0.0"

llm:
  primary:
    provider: anthropic
    model: "${primaryModel}"
    maxTokens: 16000
  fallback:
    provider: ollama
    model: llama3.2
    maxTokens: 16000

agents:
  coordinator:
    role: coordinator
    description: "Orchestrates tasks across the swarm"
    model: \${llm.primary}
    skills:
      - task-planning
      - delegation

  researcher:
    role: researcher
    description: "Gathers and synthesizes information"
    model: \${llm.primary}
    skills:
      - web-search
      - summarization

skills: []

connectors: []

dashboard:
  enabled: ${enableDashboard}
  port: ${dashboardPort}

storage:
  type: sqlite
  path: ./data/hivemind.db
`;

  fs.mkdirSync(path.join(cwd, "data"), { recursive: true });
  fs.mkdirSync(path.join(cwd, "skills"), { recursive: true });
  fs.mkdirSync(path.join(cwd, "agents"), { recursive: true });
  fs.writeFileSync(path.join(cwd, "hivemind.yaml"), configContent, "utf-8");

  if (!fs.existsSync(path.join(cwd, ".env"))) {
    fs.writeFileSync(
      path.join(cwd, ".env"),
      "# HIVEMIND Environment Variables\n# ANTHROPIC_API_KEY=\n# OPENAI_API_KEY=\n",
      "utf-8",
    );
  }

  s.succeed("Project initialized successfully");
  log("");
  info(`Config written to ${path.join(cwd, "hivemind.yaml")}`);
  info("Next steps:");
  log("  1. Set your API keys in .env");
  log('  2. Run "hivemind up" to start the swarm');
  log("");
}

