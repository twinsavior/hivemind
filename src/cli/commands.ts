  if (!codeLlm && primaryLlm) codeLlm = primaryLlm;

  // Determine display label
  const llmProvider = availableProviders.length > 0 ? availableProviders[0] : "none";
  const tandemMode = hasClaude && hasCodex;

  // ── Step 1c: Initialize persistent memory store ─────────────────────────
