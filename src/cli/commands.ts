// ── task ──────────────────────────────────────────────────────────────────────

interface TaskOptions {
  agent?: string;
  json?: boolean;
}

export async function taskCommand(taskDescription: string, options: TaskOptions): Promise<void> {
  // Submit task to running swarm via REST API
  const baseUrl = "http://localhost:4000";

  try {
    const response = await fetch(`${baseUrl}/api/tasks`, {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify({
        description: taskDescription,
        agentId: options.agent,
      }),
      signal: AbortSignal.timeout(300_000), // 5 min timeout for LLM calls
    });

    if (!response.ok) {
      const text = await response.text();
      error(`Failed to submit task: ${text}`);
      process.exit(1);
    }

    const result = await response.json() as Record<string, any>;

    if (options.json) {
      log(JSON.stringify(result, null, 2));
    } else {
      success(`Task completed by ${result["agent"] ?? "agent"}`);
      log("");
      if (result["reasoning"]) {
        info("Reasoning:");
        log(`  ${result["reasoning"]}`);
        log("");
      }
      if (result["output"]) {
        info("Output:");
        log(typeof result["output"] === "string" ? result["output"] : JSON.stringify(result["output"], null, 2));
      }
      if ((result["savedFiles"] as unknown[])?.length) {
        log("");
        success(`Files saved:`);
        for (const f of result["savedFiles"] as string[]) {
          log(`  → ${f}`);
        }
      }
    }
  } catch (err) {
    if (err instanceof TypeError && (err as any).cause?.code === "ECONNREFUSED") {
      error("Swarm is not running. Start it with: hivemind up");
    } else {
      error(err instanceof Error ? err.message : String(err));
    }
    process.exit(1);
  }
}

