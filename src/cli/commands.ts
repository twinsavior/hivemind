// ── skill add ─────────────────────────────────────────────────────────────────

interface SkillAddOptions {
  from?: string;
}

export async function skillAddCommand(
  name: string,
  options: SkillAddOptions,
): Promise<void> {
  const source = options.from ?? "registry";
  const s = spinner(`Installing skill "${name}" from ${source}...`);

  const skillsDir = path.resolve("skills");
  fs.mkdirSync(skillsDir, { recursive: true });
  const targetDir = path.join(skillsDir, name);

  if (fs.existsSync(targetDir)) {
    s.fail(`Skill "${name}" is already installed at ${targetDir}`);
    return;
  }

  try {
    if (source.startsWith("http://") || source.startsWith("https://") || source.startsWith("git@")) {
      // Clone from git URL (use spawn array form to prevent shell injection)
      s.update(`Cloning ${source}...`);
      const { spawnSync } = await import("child_process");
      const result = spawnSync("git", ["clone", "--depth", "1", source, targetDir], { stdio: "pipe" });
      if (result.status !== 0) {
        throw new Error(result.stderr?.toString().trim() || `git clone exited with code ${result.status}`);
      }
    } else if (source !== "registry" && fs.existsSync(source)) {
      // Copy from local path
      s.update(`Copying from ${source}...`);
      fs.cpSync(source, targetDir, { recursive: true });
    } else {
      // Registry: create a skill scaffold (in production, would fetch from registry API)
      s.update(`Fetching "${name}" from registry...`);
      fs.mkdirSync(targetDir, { recursive: true });
      const scaffold = `---\nname: ${name}\nversion: 1.0.0\ndescription: "${name} skill"\nagent: any\ntriggers: ["${name}"]\ntags: ["custom"]\n---\n\n# ${name}\n\nCustom skill. Edit this file to add instructions.\n`;
      fs.writeFileSync(path.join(targetDir, "SKILL.md"), scaffold, "utf-8");
    }
  } catch (err) {
    s.fail(`Failed to install skill "${name}": ${(err as Error).message}`);
    return;
  }

  s.succeed(`Skill "${name}" installed successfully`);
  info(`Skill directory: ${targetDir}/`);

  if (fs.existsSync(path.join(targetDir, "SKILL.md"))) {
    info("SKILL.md found — skill is ready to use.");
  } else {
    warn("No SKILL.md found. Create one to define the skill's behavior.");
  }
}

